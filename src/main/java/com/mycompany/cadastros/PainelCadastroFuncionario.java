    private Funcionario funcionario;
    
     @Override
    public void setDados(EntidadesVirtuais ev) {
       funcionario = (Funcionario) ev; 
       jtNome.setText(funcionario.getNome());
       jtData.setDate(funcionario.getDataNascimento());
       jtCpf.setText(funcionario.getCpf());
       jtTelefone.setText(funcionario.getTelefone());
       jtEmail.setText(funcionario.getEmail());
       
    }
       @Override
    public EntidadesVirtuais getDados() {
        if(funcionario==null)
            funcionario =new Funcionario();
        funcionario.setNome(jtNome.getText());
        funcionario.setCargo((Cargo) jComboBox1.getSelectedItem());    
        funcionario.setDataNascimento(jtData.getDate());
        funcionario.setCpf(jtCpf.getText());
        funcionario.setTelefone(jtTelefone.getText());
        funcionario.setEmail(jtEmail.getText());
        funcionario.setEndereco((Endereco) jComboBox2.getSelectedItem());
     return funcionario;  
    }

